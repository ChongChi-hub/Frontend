// Sử dụng hàm Math.abs() - Trả về giá trị tuyệt đối của số
console.log(Math.abs(-5.5)); // Kết quả: 5.5 (Vì giá trị tuyệt đối của -5.5 là 5.5)
console.log(Math.abs(10));   // Kết quả: 10 (Vì 10 đã là số dương, không thay đổi)

// Sử dụng hàm Math.ceil() - Làm tròn lên đến số nguyên lớn hơn gần nhất
console.log(Math.ceil(4.4)); // Kết quả: 5 (Vì làm tròn lên, 4.4 lên thành 5)
console.log(Math.ceil(5.8)); // Kết quả: 6 (Vì làm tròn lên, 5.8 lên thành 6)

// Sử dụng hàm Math.floor() - Làm tròn xuống đến số nguyên nhỏ hơn gần nhất
console.log(Math.floor(5.7)); // Kết quả: 5 (Vì làm tròn xuống, 5.7 xuống thành 5)
console.log(Math.floor(6.1)); // Kết quả: 6 (Vì làm tròn xuống, 6.1 xuống thành 6)

// Sử dụng hàm Math.round() - Làm tròn đến số nguyên gần nhất
console.log(Math.round(10.1)); // Kết quả: 10 (Vì 10.1 gần 10 hơn 11)
console.log(Math.round(20.6)); // Kết quả: 21 (Vì 20.6 gần 21 hơn 20)
